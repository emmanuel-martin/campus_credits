                        <div class="about-content">
                            <p>Student ratings and comments provide one source of data for review and promotion committees: The student perspective at a particular moment in time, at the end of the course. While students don’t know at this point how they’ll use what they learned or how they’ll view the course after they’ve graduated, course evaluation data are still useful, because students are experts in evaluating their experience and perceptions as learners. It is important also to provide a means for students to have a voice and for faculty to remain accountable to their students.</p>
                            <div class="row align-items-center">
                                <div class="col-12 col-md-6">
                                    <figure>
                                        <img src="assets/images/blog/about.jpeg" alt="about">
                                    </figure>
                                </div>
                                <div class="col-12 col-md-6">
                                    <p>Student evaluation data can be analyzed productively by seeking patterns and themes in the data as well as by considering it in the appropriate contexts.

                                        Questions for identifying patterns and themes in data:
                                        
                                        What patterns, if any, are indicated in numerical ratings? Ratings for some items may help to make sense of ratings for others.
                                        
                                        
                                        </p>
                                    <p>What is the teaching context? Take into account course characteristics such as size of the course, whether an instructor is co-teaching, and whether the instructor supervises TAs. How do these ratings compare to: The instructor’s other courses; ratings for courses with similar sizes, levels, or content; or other courses with similar backgrounds and preparation?
                                        What changes have occurred over time? What has improved?
                                        What ratings stand out to the instructor and why?  </p>
                                </div>
                            </div>
                            <p>Qualitative data, particularly, may help provide a more nuanced understanding of the course context or a particular issue raised in other data under review.
                                What information may help reconcile divergent ratings? Qualitative data may also help review and promotion committees interpret divergent student ratings ⁠— for example when instructors receive very high and very low rankings from different groups of students in the same course, or across multiple quarters of teaching the same course.</p>
